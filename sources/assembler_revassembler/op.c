t_op	*optab(void)
{
	return (g_op_tab);
}

t_op	get_name(int op)
{
	int	i;

	i = 0;
	while (optab()[i].name)
	{
		if (optab()[i].opcode == op)
			return (optab()[i]);
		i++;
	}
	return (optab()[i]);
}
